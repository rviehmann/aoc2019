    private static final String[] INPUT_AS_PAIRS_ARRAY = INPUT.split("\\R");

    public static class Pair {
        // Start of first assignment
        final int a;
        // End of first assignment
        final int b;
        // Start of second assignment
        final int c;
        // End of second assignment
        final int d;

        public Pair(String pair) {
            // The minus is not a sign for negativity, it just groups the numbers, so each string looks like:
            // "a-b,c-d"
            // All four numbers are positive or zero, but not negative.
            String[] numbers = pair.split("[-,]");
            this.a = parseInt(numbers[0]);
            this.b = parseInt(numbers[1]);
            this.c = parseInt(numbers[2]);
            this.d = parseInt(numbers[3]);
        }

        /**
         * @return Whether one of the two assignments fully contains the other.
         */
        public boolean hasFullContainment() {
            if (a <= c && b >= d) {
                return true;
            }
            return c <= a && d >= b;
        }

        public boolean hasOverlap() {
            for (int x = a; x <= b; x++) {
                for (int y = c; y <= d; y++) {
                    if (x == y) {
                        return true;
                    }
                }
            }
            return false;
        }
    }

