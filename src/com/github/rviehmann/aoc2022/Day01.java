    private static final String[] INPUT_AS_ELVES_ARRAY = INPUT.split("\\R\\R+");

    private static long calculateTotalCalories(String elf) {
        String[] foodItems = elf.split("\\R");
        long sum = 0;
        for (String foodItem : foodItems) {
            sum += parseLong(foodItem, 10);
        }
        return sum;
    }

    public static long doPuzzle1() {
        long max = 0;
        for (String elf : INPUT_AS_ELVES_ARRAY) {
            long calories = calculateTotalCalories(elf);
            if (calories > max) {
                max = calories;
            }
        }
        return max;
    }

    public static long doPuzzle2() {
        long[] elves = new long[INPUT_AS_ELVES_ARRAY.length];
        int index = 0;
        for (String elf : INPUT_AS_ELVES_ARRAY) {
            long calories = calculateTotalCalories(elf);
            elves[index++] = calories;
        }
        // Sorts the specified array into ascending numerical order.
        Arrays.sort(elves);
        long sum = 0;
        for (index = elves.length - 3; index <= elves.length - 1; index++) {
            sum += elves[index];
        }
        return sum;
    }
