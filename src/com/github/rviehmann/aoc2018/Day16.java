    public static class Command {
        final int opcode;
        final int A; // Input
        final int B; // Input
        final int C; // Output

        public Command(int opcode, int a, int b, int c) {
            this.opcode = opcode;
            A = a;
            B = b;
            C = c;
        }

        public Command(int[] values) {
            this.opcode = values[0];
            A = values[1];
            B = values[2];
            C = values[3];
        }
    }

    private static int[] parseBeforeOrAfter(String line) {
        String shorterLine = line
                .replace("Before: [", "")
                .replace("After:  [", "")
                .replace("]", "");
        String[] splitted = shorterLine.split(", ");
        int[] parsed = new int[splitted.length];
        for (int i = 0; i < splitted.length; i++) {
            parsed[i] = Integer.parseInt(splitted[i], 10);
        }
        return parsed;
    }

    private static Command parseCommand(String line) {
        String[] splitted = line.split(" ");
        int[] parsed = new int[splitted.length];
        for (int i = 0; i < splitted.length; i++) {
            parsed[i] = Integer.parseInt(splitted[i], 10);
        }
        return new Command(parsed);
    }

    private static Command decodeCommand(Command encodedCmd, Map<Integer, Integer> mappings) {
        if (mappings.containsKey(encodedCmd.opcode)) {
            return new Command(mappings.get(encodedCmd.opcode), encodedCmd.A, encodedCmd.B, encodedCmd.C);
        }
        throw new IllegalArgumentException("Invalid encoded command: opcode=" + encodedCmd.opcode);
    }

    private static int[] execute(int[] before, Command cmd) {
        int[] after = new int[before.length];
        System.arraycopy(before, 0, after, 0, before.length);
        switch (cmd.opcode) {
            // Addition:
            case 0: /*addr (add register)*/
                after[cmd.C] = before[cmd.A] + before[cmd.B];
                break;

            case 1: /*addi (add immediate)*/
                after[cmd.C] = before[cmd.A] + cmd.B;
                break;

            // Multiplication:
            case 2: /*mulr (multiply register)*/
                after[cmd.C] = before[cmd.A] * before[cmd.B];
                break;

            case 3: /*muli (multiply immediate)*/
                after[cmd.C] = before[cmd.A] * cmd.B;
                break;

            // Bitwise AND:
            case 4: /*banr (bitwise AND register)*/
                after[cmd.C] = before[cmd.A] & before[cmd.B];
                break;

            case 5: /*bani (bitwise AND immediate)*/
                after[cmd.C] = before[cmd.A] & cmd.B;
                break;

            // Bitwise OR:
            case 6: /*borr (bitwise OR register)*/
                after[cmd.C] = before[cmd.A] | before[cmd.B];
                break;

            case 7: /*bori (bitwise OR immediate)*/
                after[cmd.C] = before[cmd.A] | cmd.B;
                break;

            // Assignment:
            case 8: /*setr (set register)*/
                after[cmd.C] = before[cmd.A];
                break;

            case 9: /*seti (set immediate)*/
                after[cmd.C] = cmd.A;
                break;

            // Greater-than testing:
            case 10: /*gtir (greater-than immediate/register)*/
                after[cmd.C] = cmd.A > before[cmd.B] ? 1 : 0;
                break;

            case 11: /*gtri (greater-than register/immediate)*/
                after[cmd.C] = before[cmd.A] > cmd.B ? 1 : 0;
                break;

            case 12: /*gtrr (greater-than register/register)*/
                after[cmd.C] = before[cmd.A] > before[cmd.B] ? 1 : 0;
                break;

            // Equality testing:
            case 13: /*eqir (equal immediate/register)*/
                after[cmd.C] = cmd.A == before[cmd.B] ? 1 : 0;
                break;

            case 14: /*eqri (equal register/immediate)*/
                after[cmd.C] = before[cmd.A] == cmd.B ? 1 : 0;
                break;

            case 15: /*eqrr (equal register/register)*/
                after[cmd.C] = before[cmd.A] == before[cmd.B] ? 1 : 0;
                break;

            default:
                throw new IllegalArgumentException("Invalid command: opcode=" + cmd.opcode);
        }
        return after;
    }

    private static int countPossibleOpcodes(String sample, Map<Integer, Integer> alreadyKnownMappings) {
        return findPossibleOpcodes(sample, alreadyKnownMappings).size();
    }

    private static List<Integer> findPossibleOpcodes(String sample, Map<Integer, Integer> alreadyKnownMappings) {
        String[] lines = sample.split("\\R");
        int[] before = parseBeforeOrAfter(lines[0]);
        Command cmd = parseCommand(lines[1]);

        if (alreadyKnownMappings.containsKey(cmd.opcode)) {
            // No need to brute force if we already know the mapping for this opcode.
            return List.of(alreadyKnownMappings.get(cmd.opcode));
        }

        int[] after = parseBeforeOrAfter(lines[2]);
        List<Integer> possibleOpcodes = new ArrayList<>();
        for (int opcode = 0; opcode <= 15; opcode++) {
            // If this opcode is already a known mapping target, we need to ignore it.
            if (!alreadyKnownMappings.containsValue(opcode)) {
                Command cmdToExecute = new Command(opcode, cmd.A, cmd.B, cmd.C);
                int[] result = execute(before, cmdToExecute);
                if (Arrays.equals(result, after)) {
                    possibleOpcodes.add(opcode);
                }
            }
        }
        return possibleOpcodes;
    }

